package Time_ASM2;
public class Polynomial {
    public static void main(String[] args) {
        int n = 5;

        for (int i = 1; i <= n; i++) {
            for (int j = 1; j <= n; j++) {
                for (int k = 1; k <= n; k++) {
                    System.out.println("i=" + i + ", j=" + j + ", k=" + k);
                }
            }
        }
    }
}
